uint16_t ProcesarByte(uint8_t data, uint16_t crc){

  int POLY = 0x1021;
  crc = crc ^((uint16_t)data << 8);
  for(uint8_t i = 0;i<8; i++)
  {
    if(crc & 0x8000)
    {
      crc = (crc << 1) ^ POLY;
    }
    else
    {
      crc <<=1;
    }
  }
  return crc;
}

uint16_t CalcularCRC16(unsigned char* data, uint8_t longitud){

  uint16_t crc = 0;
  longitud = longitud - 3;
  while (longitud--)
  {
    crc = ProcesarByte(*data++,crc);
  }
  return crc;
}


